window.PALETTES = P;

window.useTokens = function useTokens() {
  const tw = window.__TWEAKS || {};
  const paletteKey = tw.palette || "sage";
  const paletteDef = P[paletteKey] || P.sage;
  const dark = !!tw.dark;
  const c = dark ? paletteDef.dark : paletteDef.light;
  const density = tw.density || "cozy";
  const cardStyle = tw.cardStyle || "soft";

  const pad = density === "compact" ? 16 : 24;
  const padSm = density === "compact" ? 10 : 14;
  const gap = density === "compact" ? 12 : 18;

  return {
    c,
    dark,
    palette: paletteKey,
    paletteName: paletteDef.name,
    density,
    cardStyle,
    pad,
    padSm,
    gap,
    radius: 18,
    radiusSm: 12,
    radiusLg: 24,
    font: '"Geist", -apple-system, BlinkMacSystemFont, system-ui, sans-serif',
    fontSerif: '"Newsreader", Georgia, serif',
    fontMono: '"JetBrains Mono", ui-monospace, Menlo, monospace',
    cardStyles(extra = {}) {
      const base = {
        background: c.surface,
        borderRadius: 18,
        padding: pad,
        ...extra,
      };
      if (cardStyle === "outlined") {
        return { ...base, border: `1px solid ${c.border}`, boxShadow: "none" };
      }
      if (cardStyle === "filled") {
        return {
          ...base,
          background: c.surface2,
          border: "none",
          boxShadow: "none",
        };
      }
      return {
        ...base,
        border: `1px solid ${c.border}`,
        boxShadow: dark
          ? "0 1px 2px rgba(0,0,0,.2), 0 4px 16px rgba(0,0,0,.12)"
          : "0 1px 2px rgba(40,30,20,.03), 0 4px 16px rgba(40,30,20,.03)",
      };
    },
  };
};

